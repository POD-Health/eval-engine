"""
generate_config.py

Reads questions from the PodHealth Excel sheet and generates promptfooconfig.yaml.

Usage:
    python3 podhealth/generate_config.py

The Excel file path can be set via EVAL_EXCEL_PATH in podhealth/.env
or passed as a command-line argument:
    python3 podhealth/generate_config.py path/to/questions.xlsx
"""

import os
import sys
import textwrap
from pathlib import Path
from dotenv import load_dotenv

# Load .env from same directory as this script
script_dir = Path(__file__).parent
load_dotenv(script_dir / '.env')

try:
    import openpyxl
except ImportError:
    print("ERROR: openpyxl not installed. Run: pip3 install openpyxl")
    sys.exit(1)


# ── Config ────────────────────────────────────────────────────────────────────

OUTPUT_YAML = script_dir / 'promptfooconfig.yaml'

YAML_HEADER = """\
# yaml-language-server: $schema=https://promptfoo.dev/config-schema.json
# AUTO-GENERATED by generate_config.py — do not edit manually
# To update: edit the Excel file, then run:
#   python3 podhealth/generate_config.py
#
# Run eval:
#   npm run local -- eval -c podhealth/promptfooconfig.yaml --env-file podhealth/.env --no-cache

description: PodHealth AI Agent Evaluation

providers:
  - id: 'python:provider.py'
    label: 'Piper AI Agent'

prompts:
  - '{{question}}'

defaultTest:
  options:
    provider:
      id: '{{env.GRADING_PROVIDER}}'
      config:
        apiKey: '{{env.GRADING_PROVIDER_API_KEY}}'

evaluateOptions:
  maxConcurrency: 1  # Sequential — avoids rate limits and SSE issues

tests:
"""

YAML_TEST_ENTRY = """\
  - description: '{question_id} {short_desc}'
    vars:
      question: {question}
      rubric: |
{rubric}
    assert:
      - type: llm-rubric
        value: '{{{{rubric}}}}'

"""


# ── Excel reader ──────────────────────────────────────────────────────────────

def load_questions(excel_path: str) -> list[dict]:
    """
    Read the 'Test Cases' sheet from the Excel file.
    Returns a list of dicts with keys: question_id, question, rubric
    """
    wb = openpyxl.load_workbook(excel_path)

    if 'Test Cases' not in wb.sheetnames:
        print(f"ERROR: Sheet 'Test Cases' not found in {excel_path}")
        print(f"Available sheets: {wb.sheetnames}")
        sys.exit(1)

    ws = wb['Test Cases']
    questions = []

    # Find header row (row with 'Question ID')
    header_row = None
    for row in ws.iter_rows():
        for cell in row:
            if cell.value and str(cell.value).strip() == 'Question ID':
                header_row = cell.row
                break
        if header_row:
            break

    if not header_row:
        print("ERROR: Could not find header row with 'Question ID' column")
        sys.exit(1)

    # Map column names to indices
    headers = {}
    for cell in ws[header_row]:
        if cell.value:
            headers[str(cell.value).strip()] = cell.column

    required = ['Question ID', 'Test Question', 'Expected Answer (Key Points)']
    for col in required:
        if col not in headers:
            print(f"ERROR: Required column '{col}' not found in Excel")
            print(f"Found columns: {list(headers.keys())}")
            sys.exit(1)

    qid_col = headers['Question ID']
    question_col = headers['Test Question']
    rubric_col = headers['Expected Answer (Key Points)']

    # Read data rows
    for row in ws.iter_rows(min_row=header_row + 1, values_only=True):
        qid = row[qid_col - 1]
        question = row[question_col - 1]
        rubric = row[rubric_col - 1]

        # Skip empty rows
        if not qid or not question:
            continue

        questions.append({
            'question_id': str(qid).strip(),
            'question': str(question).strip().replace('\n', ' '),
            'rubric': str(rubric).strip() if rubric else '',
        })

    return questions


# ── YAML generator ────────────────────────────────────────────────────────────

def escape_yaml_string(s: str) -> str:
    """Wrap string in single quotes, escaping any single quotes inside."""
    return "'" + s.replace("'", "''") + "'"


def format_rubric(rubric: str) -> str:
    """
    Format rubric as indented block scalar lines (8 spaces indent).
    Each bullet point on its own line.
    """
    # Normalise bullet separators
    lines = []
    for part in rubric.replace('\r\n', '\n').split('\n'):
        part = part.strip().lstrip('-').strip()
        if part:
            lines.append(f'        - {part}')

    return '\n'.join(lines) if lines else '        - (no rubric provided)'


def short_description(question: str, max_len: int = 40) -> str:
    """Generate a short description from the question text."""
    q = question.replace('\n', ' ').replace("'", '').strip()
    if len(q) > max_len:
        q = q[:max_len].rsplit(' ', 1)[0] + '...'
    return q


def generate_yaml(questions: list[dict]) -> str:
    yaml_parts = [YAML_HEADER]

    for q in questions:
        entry = YAML_TEST_ENTRY.format(
            question_id=q['question_id'],
            short_desc=short_description(q['question']),
            question=escape_yaml_string(q['question']),
            rubric=format_rubric(q['rubric']),
        )
        yaml_parts.append(entry)

    return ''.join(yaml_parts)


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    # Resolve Excel path: CLI arg > .env > default
    if len(sys.argv) > 1:
        excel_path = sys.argv[1]
    else:
        excel_path = os.environ.get(
            'EVAL_EXCEL_PATH',
            str(script_dir / 'questions.xlsx')
        )

    if not os.path.exists(excel_path):
        print(f"ERROR: Excel file not found: {excel_path}")
        print("Set EVAL_EXCEL_PATH in podhealth/.env or pass the path as an argument:")
        print("  python3 podhealth/generate_config.py path/to/file.xlsx")
        sys.exit(1)

    print(f"Reading questions from: {excel_path}")
    questions = load_questions(excel_path)
    print(f"Found {len(questions)} questions")

    yaml_content = generate_yaml(questions)

    with open(OUTPUT_YAML, 'w') as f:
        f.write(yaml_content)

    print(f"Generated: {OUTPUT_YAML}")
    print()
    print("Run eval:")
    print("  npm run local -- eval -c podhealth/promptfooconfig.yaml --env-file podhealth/.env --no-cache")


if __name__ == '__main__':
    main()
