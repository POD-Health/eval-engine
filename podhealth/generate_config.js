/**
 * generate_config.js
 *
 * Reads questions from the PodHealth Excel sheet and generates promptfooconfig.yaml.
 * Replaces generate_config.py — pure Node.js, no Python needed.
 *
 * Usage:
 *   node podhealth/generate_config.js
 *   node podhealth/generate_config.js path/to/questions.xlsx
 */

import { execFileSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// ── Install xlsx if needed ────────────────────────────────────────────────────

try {
  await import('xlsx');
} catch {
  console.log('Installing xlsx package...');
  const npm = process.platform === 'win32' ? 'npm.cmd' : 'npm';
  execFileSync(npm, ['install', 'xlsx', '--no-save'], { stdio: 'inherit' });
}

const { default: XLSX } = await import('xlsx');

// ── Load .env ─────────────────────────────────────────────────────────────────

function loadEnv(envPath) {
  if (!fs.existsSync(envPath)) {
    return;
  }
  const lines = fs.readFileSync(envPath, 'utf8').split('\n');
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }
    const idx = trimmed.indexOf('=');
    if (idx === -1) {
      continue;
    }
    const key = trimmed.slice(0, idx).trim();
    const val = trimmed
      .slice(idx + 1)
      .trim()
      .replace(/^["']|["']$/g, '');
    if (!process.env[key]) {
      process.env[key] = val;
    }
  }
}

loadEnv(path.join(__dirname, '.env'));

// ── Config ────────────────────────────────────────────────────────────────────

const OUTPUT_YAML = path.join(__dirname, 'promptfooconfig.yaml');

// ── Excel reader ──────────────────────────────────────────────────────────────

function loadQuestions(excelPath) {
  const wb = XLSX.readFile(excelPath);

  if (!wb.SheetNames.includes('Test Cases')) {
    console.error(`ERROR: Sheet 'Test Cases' not found in ${excelPath}`);
    console.error(`Available sheets: ${wb.SheetNames.join(', ')}`);
    process.exit(1);
  }

  const ws = wb.Sheets['Test Cases'];

  // The sheet has a title row first, then headers on row 2 — skip to row 3 for data
  // Use raw array mode and find the header row manually
  const rawRows = XLSX.utils.sheet_to_json(ws, { header: 1, defval: '' });

  // Find the row index that contains 'Question ID'
  const headerRowIdx = rawRows.findIndex((row) => row.includes('Question ID'));
  if (headerRowIdx === -1) {
    console.error("ERROR: Could not find header row with 'Question ID' column");
    process.exit(1);
  }

  const headers = rawRows[headerRowIdx];
  const qidCol = headers.indexOf('Question ID');
  const questionCol = headers.indexOf('Test Question');
  const rubricCol = headers.indexOf('Expected Answer (Key Points)');

  const questions = [];

  for (let i = headerRowIdx + 1; i < rawRows.length; i++) {
    const row = rawRows[i];
    const qid = String(row[qidCol] ?? '').trim();
    const question = String(row[questionCol] ?? '')
      .trim()
      .replace(/\s+/g, ' ');
    const rubric = String(row[rubricCol] ?? '').trim();

    if (!qid || !question) {
      continue;
    }

    questions.push({ qid, question, rubric });
  }

  return questions;
}

// ── YAML generator ────────────────────────────────────────────────────────────

function escapeYaml(str) {
  return "'" + str.replace(/'/g, "''") + "'";
}

function shortDesc(question, maxLen = 40) {
  const q = question.replace(/'/g, '').replace(/\s+/g, ' ').trim();
  return q.length > maxLen ? q.slice(0, maxLen).replace(/\s\S*$/, '') + '...' : q;
}

function formatRubric(rubric) {
  const lines = rubric
    .replace(/\r\n/g, '\n')
    .split('\n')
    .map((l) => l.trim().replace(/^[-•]\s*/, ''))
    .filter(Boolean);

  if (!lines.length) {
    return '        - (no rubric provided)';
  }
  return lines.map((l) => `        - ${l}`).join('\n');
}

function generateYaml(questions) {
  const gradingProvider = process.env.GRADING_PROVIDER || 'groq:llama-3.3-70b-versatile';
  const gradingApiKey = process.env.GRADING_PROVIDER_API_KEY || '';

  const header = `\
# yaml-language-server: $schema=https://promptfoo.dev/config-schema.json
# AUTO-GENERATED by generate_config.js — do not edit manually
# To update: edit the Excel file, then run:
#   npm run podhealth:eval
#
# Run eval directly:
#   npm run local -- eval -c podhealth/promptfooconfig.yaml --env-file podhealth/.env --no-cache

description: PodHealth AI Agent Evaluation

providers:
  - id: 'file://provider.py'
    label: 'Piper AI Agent'

prompts:
  - '{{question}}'

defaultTest:
  options:
    provider:
      id: '${gradingProvider}'
      config:
        apiKey: '${gradingApiKey}'

evaluateOptions:
  maxConcurrency: 1  # Sequential — avoids rate limits and SSE issues

tests:
`;

  const tests = questions
    .map(
      ({ qid, question, rubric }) => `\
  - description: ${escapeYaml(`${qid} ${shortDesc(question)}`)}
    vars:
      question: ${escapeYaml(question)}
      rubric: |
${formatRubric(rubric)}
    assert:
      - type: llm-rubric
        value: '{{rubric}}'
`,
    )
    .join('\n');

  return header + tests;
}

// ── Main ──────────────────────────────────────────────────────────────────────

const excelPath =
  process.argv[2] || process.env.EVAL_EXCEL_PATH || path.join(__dirname, 'questions.xlsx');

if (!fs.existsSync(excelPath)) {
  console.error(`ERROR: Excel file not found: ${excelPath}`);
  console.error('Set EVAL_EXCEL_PATH in podhealth/.env or pass the path as an argument:');
  console.error('  node podhealth/generate_config.js path/to/file.xlsx');
  process.exit(1);
}

console.log(`Reading questions from: ${excelPath}`);
const questions = loadQuestions(excelPath);
console.log(`Found ${questions.length} questions`);

fs.writeFileSync(OUTPUT_YAML, generateYaml(questions));
console.log(`Generated: ${OUTPUT_YAML}`);
